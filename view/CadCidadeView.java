    public CadCidade getCidade() {

        try {
            CadCidade c = new CadCidade();
            if (!jTfCodigo.getText().isEmpty()) {

                c.setCdCidade(Long.valueOf(jTfCodigo.getText()));
            }
            c.setUf((CadUf) jCBUf.getSelectedItem());

            if (jTFNome.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Informe um nome para a cidade");
                jTFNome.grabFocus();
                return null;
            }
            c.setNmCidade(jTFNome.getText());
            c.setDtTranscao(new Date());

            return c;
        } catch (Exception ex) {

            return null;
        }
    }

    public void setCidade(CadCidade cidade) {

        try {
            jTfCodigo.setText(cidade.getCdCidade().toString());
            jCBUf.setSelectedItem(cidade.getUf());
            jTFNome.setText(cidade.getNmCidade());
            jTFDataTransacao.setText(DataUtils.dateTimeToString(cidade.getDtTranscao()));
        } catch (Exception ex) {

            ex.printStackTrace();
        }

    }

    public void estadoPadraBotoes() {

        jBtNovo.setEnabled(true);
        jBtAlterar.setEnabled(true);
        jBtExcluir.setEnabled(true);

        jBtGravar.setEnabled(false);
        jBtCancelar.setEnabled(false);
    }

    public void estadoBotoes() {

        jBtNovo.setEnabled(false);
        jBtAlterar.setEnabled(false);
        jBtExcluir.setEnabled(false);

        jBtGravar.setEnabled(true);
        jBtCancelar.setEnabled(true);
    }

    public void habilitaCampos(boolean estado) {

        jTFNome.setEnabled(estado);
        jCBUf.setEditable(estado);

    }

    public void limparCampos() {

        jTFNome.setText("");
        jTfCodigo.setText("");
        jTFDataTransacao.setText("");
    }
