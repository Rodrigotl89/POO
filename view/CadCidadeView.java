    private void jBtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtGravarActionPerformed

        try {
            CadCidade c = getCidade();
            if (c == null) {
                return;
            }

            if (this.estado == Estados.getInsercao().getEstado()) {
                cDao.insert(c);
                jTfCodigo.setText(c.getCdCidade().toString());
                JOptionPane.showMessageDialog(null, "Cidade salva com sucesso!!");
            }

            if (this.estado == Estados.getAlteracao().getEstado()) {

                cDao.update(c);
                JOptionPane.showMessageDialog(null, "Cidade alterada com sucesso!!");
            }

            jTFDataTransacao.setText(DataUtils.dateTimeToString(c.getDtTranscao()));
            estadoPadraBotoes();

            habilitaCampos(false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar a cidade..:" + ex);
        }

    }//GEN-LAST:event_jBtGravarActionPerformed

    private void jBtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtExcluirActionPerformed

        try {

            CadCidade c = getCidade();
            if (c == null) {
                JOptionPane.showMessageDialog(null, "Selecione uma cidade para excluir");
                return;
            }

            if (JOptionPane.showConfirmDialog(null, "Deseja Excluir o Objeto", "Deseja Realment exlcuir a cidade?", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                this.cDao.exclurir(c);
                JOptionPane.showMessageDialog(null, "Cidade excluída com sucesso!!!");
                limparCampos();
            }

        } catch (Exception ex) {

            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao excluir cidade");
        }

    }//GEN-LAST:event_jBtExcluirActionPerformed

    private void jBTPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTPesquisarActionPerformed

        List<String> campos = new ArrayList<>();
        campos.add("CD_CIDADE");
        campos.add("NM_CIDADE");
        campos.add("NM_UF");
        campos.add("DT_TRANSACAO");

        switch (jCBTipoConsulta.getSelectedIndex()) {

            case 0: { //Geral
                jTableUtils.preencherJtable(jTable1, cDao.getAll(), campos);
            }
            break;
            case 1: {///Código

                try {

                    jTableUtils.preencherJtable(jTable1, cDao.getByCodigo(Long.parseLong(jTFValor.getText())), campos);

                } catch (Exception ex) {

                    JOptionPane.showMessageDialog(null, "Informe um valor numérico");
                }

            }
            break;
            case 2: { //Cidade
                jTableUtils.preencherJtable(jTable1, cDao.getByNmCidade(jTFValor.getText()), campos);
            }
            break;
            case 3: {///UF

                jTableUtils.preencherJtable(jTable1, cDao.getByNmUf(jTFValor.getText()), campos);
            }
            break;

        }

        cDao.fecharCursores();
    }//GEN-LAST:event_jBTPesquisarActionPerformed

